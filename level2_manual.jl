# Объяснить следующий код обработки массива names - что за number мы в итоге определили?
using Random
Random.seed!(123)
names = [rand('A':'Z') * '_' * rand('0':'9') * rand([".csv", ".bin"]) for _ in 1:100]
# ---
same_names = unique(map(y -> split(y, ".")[1], filter(x -> startswith(x, "A"), names)))
numbers = parse.(Int, map(x -> split(x, "_")[end], same_names))
numbers_sorted = sort(numbers)
number = findfirst(n -> !(n in numbers_sorted), 0:9)

#=
Что такое number?
- Из списка names отбираются только те, что начинаются с 'A'.
- У них отбрасывается расширение (например, "A_5.csv" → "A_5").
- Оставляются уникальные значения (без повторов).
- Из каждого извлекается число после подчёркивания (цифра) и преобразуется в Int.
- Полученные цифры сортируются.
- Ищется первая цифра от 0 до 9, которая отсутствует в отсортированном списке.
Таким образом, number — это наименьшая цифра от 0 до 9, не встретившаяся среди
имён, начинающихся на 'A'. Если все цифры присутствуют, number = nothing.
=# 

# Упростить этот код обработки:

digits_present = Set{Int}()
for name in names
    if startswith(name, "A")
        digit_str = split(name, "_")[2][1] 
        push!(digits_present, parse(Int, digit_str))
    end
end
number_simple = findfirst(n -> !(n in digits_present), 0:9)


